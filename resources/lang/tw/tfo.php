                    </p>
                    <p>
                        有趣的是，在心理學與社會學的研究中，獨自一人 (Alone) 與獨處 (Solitude) 對沈澱、思考、想像力、創造力、整合能力都有相當正面的影響；而一個人也與自由、深刻、啟迪等積極字詞有所關聯。換句話說，獨自一人是重要的，並與社交一樣重要。在繁忙的生活中，短暫跳脫各種責任、身份，擁有「屬於自己一人的時間」更是相當重要的喘息與充電。
                    </p>
                    <p>
                        有鑑於此，驚喜製造決定為節奏快速、行程緊湊的都市人，提供一個特別為「一個人」打造的硬體空間，並以有探索及驚喜為核心，設計出一個人也能把玩的軟性體驗。來這裡放鬆、沈澱、短暫跳脫一下生活吧！一個人很好，而且值得一個專門的空間告訴你有多好。
